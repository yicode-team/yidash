/**
 * 两数相减
 * @param {Number} x 数字 x
 * @param {Number} y 数字 y
 * @returns {Number} 返回一个数字
 * @author chensuiyi <chensuiyi.com>
 */
export function math_Minus(x, y) {
    return x - y;
}

/**
 * 两数相加
 * @param {Number} x 数字 x
 * @param {Number} y 数字 y
 * @returns {Number} 返回一个数字
 * @author chensuiyi <chensuiyi.com>
 */
export function math_Plus(x, y) {
    return x + y;
}
